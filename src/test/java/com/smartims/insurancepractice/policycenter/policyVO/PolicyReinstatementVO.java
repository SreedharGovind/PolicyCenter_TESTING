package com.smartims.insurancepractice.policycenter.policyVO;

public class PolicyReinstatementVO {
	String reinstatementReason;
	String reinstatementReasonDescription;
	String reinstatementReinstate;

	public String getReinstatementReason() {
		return reinstatementReason;
	}

	public void setReinstatementReason(String reinstatementReason) {
		this.reinstatementReason = reinstatementReason;
	}

	public String getReinstatementReasonDescription() {
		return reinstatementReasonDescription;
	}

	public void setReinstatementReasonDescription(String reinstatementReasonDescription) {
		this.reinstatementReasonDescription = reinstatementReasonDescription;
	}

	public String getReinstatementReinstate() {
		return reinstatementReinstate;
	}

	public void setReinstatementReinstate(String reinstatementReinstate) {
		this.reinstatementReinstate = reinstatementReinstate;
	}

}
